# Mathematische Funktionen zum import in calculator.py
# Additions-Funktion
def add(zahl1, zahl2):
    return zahl1 + zahl2


# Substraktions-Funktion
def subtract(zahl1, zahl2):
    return zahl1 - zahl2


# multiplikation-Funktion
def mult(zahl1, zahl2):
    return zahl1 * zahl2


# Division-Funktion
def div(zahl1, zahl2):
    return zahl1 / zahl2
